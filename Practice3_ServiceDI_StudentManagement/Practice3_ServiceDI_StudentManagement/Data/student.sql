CREATE DATABASE QLSV

CREATE TABLE SINHVIEN
(
	MSSV INT PRIMARY KEY,
	HOTENSV CHAR(30),
	GIOITINH CHAR(5),
	NGAYSINH DATE,
	LOP CHAR(5),
	KHOA INT
)

CREATE TABLE MONHOC
(
	TENMH CHAR(20),
	MAMH CHAR(10) PRIMARY KEY,
	RATEDTP DECIMAL(2,1),
	RATEDQT DECIMAL(2,1),
	SOTIET INT
)

CREATE TABLE DANGKYMH
(
	MAMH CHAR(10),
	MSSV INT,
	DQT DECIMAL(4,2),
	DTP DECIMAL(4,2),
	PRIMARY KEY (MAMH,MSSV)
)

ALTER TABLE	DANGKYMH ADD CONSTRAINT FK_DANKYMH_MONHOC FOREIGN KEY (MAMH) REFERENCES MONHOC(MAMH)

ALTER TABLE	DANGKYMH ADD CONSTRAINT FK_DANKYMH_SV FOREIGN KEY (MSSV) REFERENCES SINHVIEN(MSSV)


